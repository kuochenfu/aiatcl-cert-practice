const windowPick = <T>(pool: T[], count: number, offset: number): T[] =>
  Array.from({ length: count }, (_, index) => pool[(offset + index) % pool.length]);

const materializeQuestion = (
  draft: QuestionDraft,
  id: string,
  kind: Question["kind"],
  passageId?: string
): Question => ({
  ...draft,
  id,
  kind,
  passageId
});

const makePaper = (paperIndex: number): ExamPaper => {
  const paperNo = paperIndex + 1;
  const singleDrafts = windowPick(singlePool, 15, paperIndex * 7);
  const multipleDrafts = windowPick(multiplePool, 5, paperIndex * 3);
  const readingDrafts = windowPick(readingPool, 4, paperIndex * 2);

  const singles = singleDrafts.map((draft, index) =>
    materializeQuestion(draft, `P${paperNo}-S${String(index + 1).padStart(2, "0")}`, "single")
  );
  const multiples = multipleDrafts.map((draft, index) =>
    materializeQuestion(draft, `P${paperNo}-M${String(index + 1).padStart(2, "0")}`, "multiple")
  );
  const readingSets = readingDrafts.map((draft, setIndex) => {
    const id = `P${paperNo}-R${setIndex + 1}`;
    const questions = draft.questions.map((question, questionIndex) =>
      materializeQuestion(
        question,
        `P${paperNo}-R${setIndex + 1}-${String(questionIndex + 1).padStart(2, "0")}`,
        "reading",
        id
      )
    );
    return {
      id,
      title: draft.title,
      passage: draft.passage,
      questions
    };
  });

  return {
    id: `paper-${paperNo}`,
    title: `第 ${paperNo} 回模擬測驗`,
    description: [
      "基礎範圍",
      "治理安全",
      "企業 GenAI",
      "邊緣與數位孿生",
      "綜合衝刺"
    ][paperIndex],
    updatedContext: "依 2026 可查公開資訊補入 AI 治理、RAG、LLMOps 與 LLM 安全情境。",
    sourceNotes: paperSourceNotes,
    readingSets,
    questions: [...singles, ...multiples, ...readingSets.flatMap((set) => set.questions)]
  };
};

export const examPapers: ExamPaper[] = Array.from({ length: 5 }, (_, index) => makePaper(index));

export const readingSets: ReadingSet[] = examPapers[0].readingSets;
export const allQuestions: Question[] = examPapers[0].questions;
